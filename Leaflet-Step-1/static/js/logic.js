function createCircleMarker(earthquake, latlng) {

    console.log(earthquake)

    // Conditionals for countries points
    var color = "";
    if (earthquake.properties.mag > 0 && earthquake.properties.mag <= 1) {
        color = "yellow";
    }
    else if (earthquake.properties.mag > 1 && earthquake.properties.mag <= 2) {
        color = "blue";
    }
    else if (earthquake.properties.mag > 2 && earthquake.properties.mag <= 3) {
        color = "green";
    }
    else if (earthquake.properties.mag > 3 && earthquake.properties.mag <= 4) {
        color = "orange";
    }
    else if (earthquake.properties.mag > 4 && earthquake.properties.mag <= 5) {
        color = "darkorange";
    }
    else {
        color = "red";
    }

    var geojsonMarkerOptions = {
        radius: 8,
        fillColor: color,
        color: "#000",
        weight: 1,
        opacity: 1,
        fillOpacity: 0.8
    };

    // Add circles to map
    return L.circleMarker(latlng, geojsonMarkerOptions);
}